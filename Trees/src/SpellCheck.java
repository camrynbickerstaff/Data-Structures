	public static void main(String[] args) throws Exception
	{
		// Reading the text file
		File file = new File("dictionary.txt");
		Scanner input = new Scanner(file);
		BinarySearchTree<String> dictionary = new BinarySearchTree<String>();
		
		while(input.hasNext())
		{
			dictionary.insert(input.next().toLowerCase());
		}
		Scanner check = new Scanner(new File("sampleText.txt"));
		String wholeThing = "";
		while(check.hasNext())
		{
			String s = check.next();
			wholeThing = wholeThing + "\n" + s;
			if(!dictionary.searchFor(s.toLowerCase()))
			{
				System.out.println("\"" + s + "\" is spelled incorrectly.");
			}
			
		}
		System.out.println("\nHere are all of the words you wanted spell checked: " + wholeThing + "\n");
		Thread.sleep(5000);
		prepare();
		Thread.sleep(5000);
		ajwa();
		input.close();
		check.close();
		
	}

	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
